//////////////////////////////////////////////////////////
//                      Requires                        //
//////////////////////////////////////////////////////////
var bodyParser      = require("body-parser"),
    express         = require("express"),
    path            = require("path"),
    port            = require(path.join(__dirname,"./server/config/settings.js")).port;
    app             = express();

////////////////////////////////////////////////////////////
//             App.use (Body Parser, Static)              //
////////////////////////////////////////////////////////////
app.use(bodyParser.json());
app.use(express.static(path.join(__dirname, "./client")));

////////////////////////////////////////////////////////////
//                        Mongoose                        //
////////////////////////////////////////////////////////////
require(path.join(__dirname, './server/config/mongoose.js'));
////////////////////////////////////////////////////////////
//                         Routes                         //
////////////////////////////////////////////////////////////
require(path.join(__dirname, './server/config/routes.js'))(app)

////////////////////////////////////////////////////////////
//                     Listen to Port                     //
////////////////////////////////////////////////////////////
app.listen(port, function() {
    console.log("1216 FullMean Demo! ("+port+")");
})
